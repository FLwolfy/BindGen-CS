using Xunit;

namespace BGCS.Tests;

public class GeneratedCodeCompilationMatrixTests
{
    [Fact]
    public void Generate_Matrix_OutputShouldCompileAndExposeExpectedSemantics()
    {
        foreach (var scenario in CreateScenarios())
        {
            var run = RunGenerator(scenario);
            try
            {
                AssertGeneratorSucceeded(scenario.Name, run.Ok, run.Messages);

                if (scenario.ExpectSingleFileOutput)
                {
                    string[] files = Directory.GetFiles(run.OutputPath, "*.cs", SearchOption.AllDirectories);
                    Assert.Single(files);

                    string merged = File.ReadAllText(files[0]);
                    Assert.Contains("partial struct", merged, StringComparison.Ordinal);
                    Assert.Contains("Native", merged, StringComparison.Ordinal);
                    Assert.Equal(1, CountAutoGeneratedCommentBlocks(merged));
                }

                Assembly asm = CompileGeneratedSources(run.OutputPath, "BGCS.Generated.Matrix." + scenario.Name);

                for (int i = 0; i < scenario.ExpectedTypeNames.Count; i++)
                {
                    Assert.NotNull(asm.GetType($"{scenario.Namespace}.{scenario.ExpectedTypeNames[i]}"));
                }

                for (int i = 0; i < scenario.ExpectedMethodNames.Count; i++)
                {
                    MethodInfo? method = FindMethodByName(asm, scenario.ExpectedMethodNames[i]);
                    Assert.NotNull(method);
                }

                scenario.VerifyAssembly?.Invoke(asm, run);
            }
            finally
            {
                Cleanup(run.TempDirectory);
            }
        }
    }

    private static List<CompileScenario> CreateScenarios()
    {
        return
        [
            new()
            {
                Name = "C_Primitives_And_Struct",
                Header = """
                    typedef struct BgcsPoint
                    {
                        int x;
                        int y;
                    } BgcsPoint;

                    int bgcs_add(int a, int b);
                    void bgcs_update(BgcsPoint* p);
                    """,
                ImportType = ImportType.DllImport,
                ExpectedTypeNames = ["BgcsPoint"],
                ExpectedMethodNames = ["BgcsAddNative", "BgcsUpdateNative"]
            },
            new()
            {
                Name = "C_Enum_Macro_Callback",
                Header = """
                    #define BGCS_CONST_A 42
                    typedef enum BgcsMode
                    {
                        BGCS_MODE_A = 0,
                        BGCS_MODE_B = 1
                    } BgcsMode;

                    typedef void (*BgcsCallback)(int value);
                    void bgcs_set_mode(BgcsMode mode);
                    void bgcs_set_callback(BgcsCallback callback);
                    """,
                ImportType = ImportType.DllImport,
                ExpectedTypeNames = ["BgcsMode", "BgcsCallback"],
                ExpectedMethodNames = ["BgcsSetModeNative", "BgcsSetCallbackNative"]
            },
            new()
            {
                Name = "Cpp_Extern_C",
                Header = """
                    namespace Hidden
                    {
                        class IgnoredClass
                        {
                        public:
                            int value;
                        };
                    }

                    extern "C"
                    {
                        int bgcs_from_cpp(int a);
                    }
                    """,
                ImportType = ImportType.DllImport,
                ExpectedTypeNames = [],
                ExpectedMethodNames = ["BgcsFromCppNative"]
            },
            new()
            {
                Name = "FunctionTable_Mode",
                Header = """
                    int bgcs_ping(void);
                    int bgcs_add2(int a, int b);
                    """,
                ImportType = ImportType.FunctionTable,
                ExpectedTypeNames = ["CompileApi"],
                ExpectedMethodNames = ["BgcsPingNative", "BgcsAdd2Native", "InitApi", "FreeApi"]
                ,
                Configure = cfg =>
                {
                    cfg.UseCustomContext = true;
                }
            },
            new()
            {
                Name = "SingleFile_Merge",
                Header = """
                    typedef struct BgcsValue
                    {
                        int v;
                    } BgcsValue;
                    int bgcs_read(BgcsValue* value);
